#nullable enable

namespace Nighthollow.Model
{
  /// <summary>
  /// Represents a template for generating a random affix. The values in "Low" correspond to the lowest-possible numeric
  /// stat values to generate for this affix, while the values in "high" represent the highest-possible numeric values.
  /// Typically, an Affix is generated by selecting a value within this range uniformly at random.
  /// </summary>
  public sealed class AffixTemplateData
  {
    public AffixData Low { get; }
    public AffixData High { get; }

    public AffixTemplateData(AffixData low, AffixData high)
    {
      Low = low;
      High = high;
    }

    public AffixTemplateData Clone() => new AffixTemplateData(Low.Clone(), High.Clone());
  }
}