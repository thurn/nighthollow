#nullable enable

namespace Nighthollow.Model
{
  /// <summary>
  /// Represents a template for generating a random card. The values in "Low" correspond to the lowest-possible numeric
  /// stat values to generate for this card, while the values in "high" represent the highest-possible numeric values.
  /// Typically, a Card Item is generated by selecting a value within this range uniformly at random.
  /// </summary>
  public sealed class CardTemplateData
  {
    public CardItemData Low { get; }
    public CardItemData High { get; }

    public CardTemplateData(CardItemData low, CardItemData high)
    {
      Low = low;
      High = high;
    }

    public CardTemplateData Clone() => new CardTemplateData(Low.Clone(), High.Clone());
  }
}